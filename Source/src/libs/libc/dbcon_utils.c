static int DBConDoRefresh(int nIndexCtx)
{

	int				nResult;
	char			sLine[1024];
	time_t			start_time,end_time;


	if (ToFullTrace(nIndexCtx) == OK)
	{
		trace_event("Start DBConDoRefresh()", PROCESSING);
	}



	time(&start_time);
	nResult = RefreshDBConnection(nIndexCtx);
	time(&end_time);


	if ( nResult != OK)
	{
		/*if the request took too much time, do nothing. The requester thread should wake up and automatically renew the db connection*/
		if( end_time - start_time > 1 )
		{
			sprintf(sLine, "DB_CONN_REFRESH: Too much time to serve the request for ctx %d(%lu)", nIndexCtx, end_time - start_time);
			trace_event(sLine,ERROR);
			nResult = NOK;
		}
		else
		{
			sprintf(sLine,"DB_CONN_REFRESH: Refresh DB Connection by Exception for ctx %d",nIndexCtx);
			trace_event(sLine,ERROR);
			DeconnectOracle(nIndexCtx);
			/*EBE160628 */
			nResult = ConnectOracle(nIndexCtx);
			if( nResult != OK )
			{
				nResult = NOK;
			}
		}

	}

	if (ToFullTrace(nIndexCtx) == OK )
	{
		trace_event("End   DBConDoRefresh(OK)", PROCESSING);
	}

	/*EBE160628 
	return OK;*/
	return nResult;

}
/******************************************************************************/
int	ProcessDBConRequest(int nIndex)
{
	time_t			nCurrTime;
	int				nResult;
	int				nRefreshResult;
	char			sLine[1024];
	TSParamCtx		ParamCtx;


	if (tab_DBConThread[nIndex].nFullTrace == OK)
	{
		sprintf(sLine, "Start ProcessDBConRequest(RefThrIdx(%d),CtxIdx(%d), ProcThrId(%.08lu)", nIndex, tab_DBConThread[nIndex].nIndex, tab_DBConThread[nIndex].nThreadId);
		trace_event(sLine, PROCESSING);
	}

	/* we need to keep a backup of the ctx in case we have to free it*/
	/*ParamCtx = tab_ParamCtx[tab_DBConThread[nIndex].nIndex];*/
	ParamCtx = tab_DBConThread[nIndex].ParamCtx;/*ICH20170830*/

	time(&nCurrTime);

	/* we have a request to serve but first check if the request has expired*/
	if( tab_DBConThread[nIndex].nExpiryTime < nCurrTime )
	{
		sprintf(sLine,"End   ProcessDBConRequest(TOOLATE) for ctx %d",tab_DBConThread[nIndex].nIndex);
		trace_event (sLine , ERROR );
		return OK;
	}

	nRefreshResult = DBConDoRefresh(tab_DBConThread[nIndex].nIndex);

	/* Make sure to not signal until the requester go to sleep */
	nResult = pthread_mutex_lock (&(tab_DBConThread[nIndex].ResultMutex));
	if(nResult == EDEADLK)
	{
		trace_event ( "End   ProcessDBConRequest(EDEADLK)", ERROR );
		return NOK;
	}

	if( nRefreshResult == OK )
	{
		tab_DBConThread[nIndex].nConState = CON_ST_REFRESHED;
	}
	else
	{
		tab_DBConThread[nIndex].nConState = CON_ST_REFRESH_FAILURE;
	}

	/* if by this time, the requester already abondonned this ctx, then we need to free it */
	if( tab_DBConThread[nIndex].nCtxState	== CTX_ST_DROPPED )
	{
		sprintf(sLine,">>> Free Dropped ctx %d",tab_DBConThread[nIndex].nIndex);
		trace_event ( sLine, ERROR );
		DeconnectOracleParamCtx(&ParamCtx);
		FreeOracleParamCtx(&ParamCtx);
	}

	nResult = pthread_cond_signal( &(tab_DBConThread[nIndex].ResultCond));
	nResult = pthread_mutex_unlock (&(tab_DBConThread[nIndex].ResultMutex));


	if (tab_DBConThread[nIndex].nFullTrace == OK)
	{
		trace_event("End   ProcessDBConRequest(OK)", PROCESSING);
	}

	return OK;
}


/******************************************************************************\
* This function is to be called by an interface thread to check
* and renew its connection
* ICH20160729 return ORA error in the case of problem with DB
*
\******************************************************************************/
int LitecheckAndRefreshDBCon(int nIndexCtx, struct timeval	lastTime)
{

	struct timeval		currTime;
	int					nThreadIndex;
	int					nResult;
	struct timespec		maxTimeToServe;
	struct timeval  	tv;
	char				sLine[1024];
	int					nFullTrace = OK;
	int					nRefreshResult = OK;
	int					nORAError;

	if (ToFullTrace(nIndexCtx) == OK )
	{
		nFullTrace = OK;
	}
	else
	{
		nFullTrace = NOK;
	}

	if (nFullTrace == OK)
	{
		sprintf(sLine, "Start CheckAndRefreshDBCon(IdxCtx(%d), ThrId(%.08lu))", nIndexCtx, pthread_self());
		trace_event(sLine, PROCESSING);
	}

	gettimeofday(&currTime,NULL);
	/* Check if a "long" time elapsed from the last time we used the DB connection */

/*ICH	if( currTime.tv_sec - lastTime.tv_sec < MAX_DB_INACTIV_PERIOD )
	{
		trace_event ( "End   CheckAndRefreshDBCon(OK): Nothing to be done", PROCESSING );
		return OK;
	}*/

	/* Try to get a free thread to refresh our connection*/
	nThreadIndex = AllocDBConThread();
	if( nThreadIndex < 0 )
	{

		sprintf(sLine,">>>>> No thread available -> Self refresh for ctx %d",nIndexCtx);
		trace_event (sLine , ERROR );
		/* If can't get a thread to do this, then just renew the connection ourself */
		DeconnectOracle(nIndexCtx);
		FreeOracleCtx(nIndexCtx); /*EBE140131*/


		AllocateOracleCtx(nIndexCtx); /*EBE140131*/

		nORAError=ConnectOracle(nIndexCtx);

		if (nFullTrace == OK)
		{
			trace_event("End   CheckAndRefreshDBCon(OK)", PROCESSING);
		}

		return nORAError;

	}
	
	if (gettimeofday(&tv, NULL) < 0) 
	{
		fprintf(stderr, "gettimeofday Error : %d\n", errno);
		return (NOK);
	}

	/*EBE140905: set our state as waiting response from the refresher*/
	nResult = pthread_mutex_lock(&(tab_DBConThread[nThreadIndex].CtxOwnerStMutex));
	tab_DBConThread[nThreadIndex].nCtxOwnerState = OWNER_ST_WAITING_RESPONSE;
	pthread_mutex_unlock(&(tab_DBConThread[nThreadIndex].CtxOwnerStMutex));


	/*Build the request parameters and inform the refresher to start refreshing the DB connection*/
	nResult = pthread_mutex_lock (&(tab_DBConThread[nThreadIndex].ResultMutex));
	if(nResult == EDEADLK)
	{
		trace_event ( "End   CheckAndRefreshDBCon(EDEADLK on ResultMutex)", ERROR );
		pthread_mutex_unlock(&(tab_DBConThread[nThreadIndex].ResultMutex));
		return NOK;
	}
	
	
	maxTimeToServe.tv_sec = tv.tv_sec + MAX_TIME_TO_REFRESH/1000;
	maxTimeToServe.tv_nsec = (tv.tv_usec *1000) + (MAX_TIME_TO_REFRESH%1000) * 1000000;
	if( maxTimeToServe.tv_nsec > 1000000000 )
	{
		maxTimeToServe.tv_nsec -= 1000000000;
		maxTimeToServe.tv_sec++;
	}


	tab_DBConThread[nThreadIndex].nConState		= CON_ST_TO_REFRESH;
	tab_DBConThread[nThreadIndex].nCtxState		= CTX_ST_NORMAL;
	tab_DBConThread[nThreadIndex].nIndex		= nIndexCtx;
	tab_DBConThread[nThreadIndex].nExpiryTime	= maxTimeToServe.tv_sec;
	tab_DBConThread[nThreadIndex].nThreadId		= p7_thread_id();
	tab_DBConThread[nThreadIndex].nFullTrace	= nFullTrace;
	tab_DBConThread[nThreadIndex].ParamCtx		= tab_ParamCtx[nIndexCtx];/*ICH20170830*/


	InfoDBConThread(nThreadIndex);

	/*Waiting for a response from the refresher*/
	while ( (tab_DBConThread[nThreadIndex].nConState == CON_ST_TO_REFRESH))
	{
		nResult = pthread_cond_timedwait(&(tab_DBConThread[nThreadIndex].ResultCond),
			&(tab_DBConThread[nThreadIndex].ResultMutex),&maxTimeToServe);
		if( nResult == ETIMEDOUT )
		{
			sprintf(sLine,"DB_CONN_REFRESH: TIMEOUT ON REFRESH for ctx %d(%d)(%d)",nIndexCtx,tab_DBConThread[nThreadIndex].nConState,nThreadIndex);
			trace_event ( sLine, ERROR );
			break;
		}
		else if( nResult != 0 )
		{
			/*sprintf(sLine,"DB_CONN_REFRESH: ERROR WAIT ON REFRESH for ctx %d(%d)(%d)",nIndexCtx,tab_DBConThread[nThreadIndex].nConState,nThreadIndex);*/
			sprintf(sLine,"DB_CONN_REFRESH: ERROR(%d,%d) WAIT ON REFRESH for ctx %d(%d)(%d)",nResult, errno, nIndexCtx,tab_DBConThread[nThreadIndex].nConState,nThreadIndex);
			trace_event (sLine , ERROR );
			break;
		}
	}



	if( tab_DBConThread[nThreadIndex].nConState == CON_ST_REFRESH_FAILURE ||
		tab_DBConThread[nThreadIndex].nConState == CON_ST_TO_REFRESH )
	{
		sprintf(sLine,">>>>> External process failed to refresh -> local refresh for ctx %d(%d)(%d)",nIndexCtx,tab_DBConThread[nThreadIndex].nConState,nThreadIndex);
		trace_event (sLine , ERROR );
		/* EBE140422: This will be done by refresher thread, we just allocate a new ctx
		DeconnectOracle(nIndexCtx);
		FreeOracleCtx(nIndexCtx); *EBE140131*
		*/

		/*EBE160628: we may have a leak in case of CON_ST_REFRESH_FAILURE. No thread will free the ctx*/
		if( tab_DBConThread[nThreadIndex].nConState == CON_ST_REFRESH_FAILURE )
		{
			DeconnectOracle(nIndexCtx);
			FreeOracleCtx(nIndexCtx);
		}
		else
		{
			tab_DBConThread[nThreadIndex].nCtxState	= CTX_ST_DROPPED;
		}

		AllocateOracleCtx(nIndexCtx); /*EBE140131*/
		nORAError = ConnectOracle(nIndexCtx);
	}
	else if( tab_DBConThread[nThreadIndex].nConState == CON_ST_REFRESHED )
	{
		if (tab_DBConThread[nThreadIndex].nFullTrace == OK)
		{
			sprintf(sLine, ">>> Refresh Success %d(used %d)", nIndexCtx, nThreadIndex);
			trace_event(sLine, ERROR);
		}
		nORAError = OK;
	}
	tab_DBConThread[nThreadIndex].nConState = CON_ST_NONE;
	pthread_mutex_unlock (&(tab_DBConThread[nThreadIndex].ResultMutex));

	/*EBE140905*/
	nResult = pthread_mutex_lock(&(tab_DBConThread[nThreadIndex].CtxOwnerStMutex));
	tab_DBConThread[nThreadIndex].nCtxOwnerState = OWNER_ST_DONE;
	pthread_mutex_unlock(&(tab_DBConThread[nThreadIndex].CtxOwnerStMutex));


	if (nFullTrace == OK)
	{
		trace_event("End   CheckAndRefreshDBCon(OK)", PROCESSING);
	}

	return nORAError;
}
