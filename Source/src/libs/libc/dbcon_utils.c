static int AllocDBConThread()
{
	int		i=0;
	int		nIndex;


	/*trace_event ( "Start AllocDBConThread()", PROCESSING );*/
	/* Get a free thread or create one if non exceeding the maximum */
	pthread_mutex_lock(&TabMutex);
	for(i=0; i < nNbThreads; i++ )
	{
		if( tab_DBConThread[i].nState == DBCON_ST_FREE )
		{
			tab_DBConThread[i].nState = DBCON_ST_ASSIGNED;
			pthread_mutex_unlock(&TabMutex);
			/*trace_event ( "End   AllocDBConThread(OK)", PROCESSING );*/
			return i;
		}
	}


	if( nNbThreads < MAX_DBCON_THREADS )
	{
		nIndex = CreateDBConThread();
		tab_DBConThread[nIndex].nState = DBCON_ST_ASSIGNED;
		pthread_mutex_unlock(&TabMutex);
		/* Need to sleep a little bit to let the thread initialize itself*/
		sleep(1);
		trace_event ( "End   AllocDBConThread(OK): Creating thread", PROCESSING );
		return nIndex;
	}

	pthread_mutex_unlock(&TabMutex);
	trace_event ( "End   AllocDBConThread(NONE)", ERROR );
	return -1;
}
