unsigned char Ascii_To_Hex(c)
char c;
{
   if ( (c>= '0') && (c<='9'))
      return (c-'0');

   if ( (c>= 'A') && (c<='F'))
      return (c-'A'+10);

   if ( (c>= 'a') && (c<='f'))
      return (c-'a'+10);
   return(0x00);
}

unsigned char Hex_To_Ascii(c)
char  c;
{
      if (c <= 0x09) return (c + 0x30);
      else
      {
         if (c == 0x0A) return ( 0x41);
         else if (c == 0x0B) return ( 0x42);
         else if (c == 0x0C) return ( 0x43);
         else if (c == 0x0D) return ( 0x44);
         else if (c == 0x0E) return ( 0x45);
         else if (c == 0x0F) return ( 0x46);
      }
     return(0x00);
}

int HexToAscii (const BYTE *sSrc, BYTE *sDst, int nLng)
{

   int  i = 0;
   int  j = 0;
   int   nExtraZero = 0;
   char sWork [256];

   memset(sWork, '\0', sizeof(sWork));

   if (nLng % 2){
      nLng += 1;
      nExtraZero = 1;
   }

   for (i=0; i<nLng/2; i++){
      sWork[j++] = Hex_To_Ascii( ((sSrc[i] & 0xf0) >> 4)) ;
      sWork[j++] = Hex_To_Ascii( (sSrc[i] & 0x0f ) ) ;
   }

   if (nExtraZero)
      memcpy(sDst, sWork+1, j);
   else
      memcpy(sDst, sWork, j);

   return (nLng/2);
}

int AsciiToHex (const BYTE *sSrc, BYTE *sDst, int nLng)
{
   int  i, j;
   char cLeft, cRight;
   char sWorkSrc [256];
   char sWorkDst [256];

   memset(sWorkSrc, '0', sizeof(sWorkSrc));
   memset(sWorkDst, '\0', sizeof(sWorkDst));

   if (nLng % 2){
      memcpy(sWorkSrc+1, sSrc, nLng);
      nLng += 1;
   }
   else
      memcpy(sWorkSrc, sSrc, nLng);

   for (i=0,j=0; i<nLng; i++){
      cLeft  = Ascii_To_Hex(sWorkSrc[j++]) << 4;
      cRight = Ascii_To_Hex(sWorkSrc[j++]) & 0x0F ;
      sWorkDst[i] = cLeft | cRight;
   }

   memcpy(sDst, sWorkDst, i/2);

   return(i/2);            /* Number of Bytes */
}

