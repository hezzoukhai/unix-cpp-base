char compute_LRC(buffer, lg)
char        *buffer;
int     lg;
{
int            Index;
unsigned char  LRC_BYTE = 0xFF;

for(Index=0; Index<lg; Index++)
    LRC_BYTE ^= *(buffer + Index);
LRC_BYTE &= 0x7F;

return( (char)LRC_BYTE );
}

