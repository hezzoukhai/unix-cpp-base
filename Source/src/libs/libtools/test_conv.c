int SetBit ( char *sBitMap, int nBitNbr)
{
/*
   if (nBitNbr > 128)
      return(-nBitNbr);  */     /* Invalid bit number nBitNbr */

   --nBitNbr;                  /* adjust bit number for base 0 */
   /* set bit to high state */
   sBitMap [nBitNbr / 8] |= (0x80 >> (nBitNbr % 8));
   return(0);
}

int ClearBit ( char *sBitMap, int nBitNbr)
{
   /* if (nBitNbr > 128)
      return(-nBitNbr); */      /* Invalid bit number nBitNbr */

   --nBitNbr;                  /* adjust bit number for base 0 */
   /* set bit to low  state */
   sBitMap [nBitNbr / 8] &= ~(0x80 >> (nBitNbr % 8));
   return(0);
}

int CheckBit( char *cPtr, int nBitNbr )
{
   cPtr += (nBitNbr / 8);
   return( ( (*cPtr) >> (7-nBitNbr % 8) ) & 0x01 );
}

int FieldPresent( char *cPtr, int nFldNbr)
{
   return ( CheckBit(cPtr, nFldNbr-1) );
}

